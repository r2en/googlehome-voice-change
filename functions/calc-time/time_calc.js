function _average(data){
    var sum = 0;
    for(i = 0; i < data.length; i++){
        sum += data[i];
    }
    return (sum / data.length);
}

function _variance(data){
    var average = _average(data);
    var variance = 0;
    for(i = 0; i < data.length; i++){
        variance = variance + Math.pow(data[i] - average, 2);
    }
    return (variance / data.length);
}

function _stardart_deviation(data){
    var variance = _variance(data);
    return Math.sqrt(variance)
}

